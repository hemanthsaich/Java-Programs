
public class checkPositive {

	public static void main(String[] args) {
		int n = 0;
		if(n > 0) {
			System.out.println("Positive");
		}else if(n < 0){
			System.out.println("Negative");
		}else if(n == 0) {
			System.out.println("Zero");
		}else {
			System.out.println("Enter a valid number.");
		}

	}

}
