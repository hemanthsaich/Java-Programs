
public class GreaterInTwo {

	public static void main(String[] args) {
		int a = 13;
		int b = 10;
		if (a == b) {
			System.out.println("Both are equal. \n");
		}else if (a > b) {
			System.out.println(a+ " is greater than " +b);
		}else {
			System.out.println(b+ " is greater than " +a);
		}
	}

}
